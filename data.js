function isConfirmedDirectoryItem(item) {
  const officialStatus = item?.officialVerification?.status;
  const hasReviewEvidence = Array.isArray(item?.externalReviews) && item.externalReviews.length > 0;
  return officialStatus === "verified" ||
    item?.sourceStatus === "기본 정보 확인" ||
    item?.sourceStatus === "기본 정보 등록" ||
    (item?.sourceStatus === "상품 정보 확인" && hasReviewEvidence && item?.publicationStatus === "published") ||
    item?.verificationStatus === "verified" ||
    item?.verificationStatus === "reviewed" ||
    item?.verificationStatus === "partner-reviewed" ||
    item?.confirmedBy === "admin" ||
    item?.confirmedBy === "vendor";
}

function toPublicDirectoryItem(item) {
  const isReviewBased = item?.sourceStatus === "상품 정보 확인";
  const reviewCount = Array.isArray(item?.externalReviews) ? item.externalReviews.length : 0;
  return {
    ...item,
    sourceStatus: isReviewBased ? "후기 기반 등록" : item.sourceStatus,
    intro: isReviewBased
      ? `${item.area || item.region || "활동 지역"}에서 ${item.subcategory || item.category} 관련 후기가 모인 파트너입니다. 행사 유형과 준비 항목을 같은 기준으로 정리했습니다.`
      : item.intro,
    detailFacts: {
      ...(item.detailFacts || {}),
      ...(isReviewBased ? {
        "후기 확인": `${reviewCount.toLocaleString("ko-KR")}개 원문 연결`,
        "등록 기준": "공개 후기 기반"
      } : {})
    },
    detailUrl: item.detailUrl || `provider.html?id=${encodeURIComponent(item.id)}`
  };
}

